export const getBibleIndex = (keyword) => {
  let index = -1

  // Process Space
  keyword = keyword.split(' ').join('')

  // Search IDN
  BIBLE_INDEXES
    .map(x => x.keyword.split(' ').join('').toLowerCase())
    .some(
      (value, i) => {
        if (value.includes(keyword.toLowerCase())) {
          index = i
          return true
        }
        return false
      }
    )

  // Search Jpn if Idn not found
  if (index === -1) {
    BIBLE_INDEXES
      .map(x => x.jp.split(' ').join(''))
      .forEach(
        (value, i) => {
          if (value.includes(keyword)) {
            index = i
            return true
          }
          return false
        }
      )
  }

  // Error if not Found
  if (index === -1) {
    throw 'Index Not Found'
  }

  // add book index
  const bibleIndex = { ...BIBLE_INDEXES[index], bookId: index + 1}

  return bibleIndex
}